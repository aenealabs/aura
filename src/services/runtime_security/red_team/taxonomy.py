"""
Project Aura - AURA-ATT&CK Threat Taxonomy

97 adversarial techniques across 11 MITRE-style categories for
automated red teaming of agentic AI systems.

Based on ADR-083: Runtime Agent Security Platform

Categories:
  ATA-01: Prompt Injection (12 techniques)
  ATA-02: Tool Abuse (10 techniques)
  ATA-03: Agent Confusion (9 techniques)
  ATA-04: Data Exfiltration (8 techniques)
  ATA-05: Privilege Escalation (10 techniques)
  ATA-06: Denial of Service (8 techniques)
  ATA-07: Supply Chain (9 techniques)
  ATA-08: Evasion (9 techniques)
  ATA-09: Cryptographic Weaknesses (8 techniques)
  ATA-10: Memory Safety (8 techniques)
  ATA-11: Sandbox/Isolation Escape (6 techniques)

Compliance:
- NIST 800-53 CA-8: Penetration testing
- NIST 800-53 SI-4: Information system monitoring
- MITRE ATT&CK Framework alignment
"""

from dataclasses import dataclass
from enum import Enum
from typing import Any, Optional


class AttackCategory(Enum):
    """AURA-ATT&CK attack categories."""

    PROMPT_INJECTION = "ATA-01"
    TOOL_ABUSE = "ATA-02"
    AGENT_CONFUSION = "ATA-03"
    DATA_EXFILTRATION = "ATA-04"
    PRIVILEGE_ESCALATION = "ATA-05"
    DENIAL_OF_SERVICE = "ATA-06"
    SUPPLY_CHAIN = "ATA-07"
    EVASION = "ATA-08"
    CRYPTOGRAPHIC_WEAKNESSES = "ATA-09"
    MEMORY_SAFETY = "ATA-10"
    SANDBOX_ESCAPE = "ATA-11"


class TechniqueComplexity(Enum):
    """Complexity level for executing the technique."""

    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"
    EXPERT = "expert"


@dataclass(frozen=True)
class AttackTechnique:
    """Immutable definition of an AURA-ATT&CK technique."""

    technique_id: str
    name: str
    category: AttackCategory
    description: str
    complexity: TechniqueComplexity
    mitre_attack_ids: tuple[str, ...]
    nist_controls: tuple[str, ...]
    detection_points: tuple[str, ...]
    example_payload: str
    expected_behavior: str
    remediation: str

    def to_dict(self) -> dict[str, Any]:
        """Serialize to dictionary."""
        return {
            "technique_id": self.technique_id,
            "name": self.name,
            "category": self.category.value,
            "description": self.description,
            "complexity": self.complexity.value,
            "mitre_attack_ids": list(self.mitre_attack_ids),
            "nist_controls": list(self.nist_controls),
            "detection_points": list(self.detection_points),
            "example_payload": self.example_payload,
            "expected_behavior": self.expected_behavior,
            "remediation": self.remediation,
        }


