class TestIdempotence:
    """Running the stripper twice must be a no-op on the second pass."""

    def test_idempotent_run(self):
        msg = (
            "feat: thing\n"
            "\n"
            "Co-Authored-By: Claude <noreply@anthropic.com>\n"
            "🤖 Generated with [Claude Code](https://claude.com/claude-code)\n"
        )
        cleaned_once, removed_first = commit_msg_hook.strip_ai_attribution(msg)
        cleaned_twice, removed_second = commit_msg_hook.strip_ai_attribution(
            cleaned_once
        )
        assert removed_first >= 2
        assert removed_second == 0
        assert cleaned_once == cleaned_twice


class TestPreservesContent:
    """Stripping must not damage non-AI content."""

    def test_preserves_multiparagraph_body(self):
        body = (
            "feat(scanner): wire up X\n"
            "\n"
            "Implements the Y behavior described in ADR-099.\n"
            "Now does Z when triggered by W.\n"
            "\n"
            "Closes #1234.\n"
            "\n"
            "Co-Authored-By: Claude <noreply@anthropic.com>\n"
        )
        cleaned, removed = commit_msg_hook.strip_ai_attribution(body)
        assert removed == 1
        assert "Implements the Y behavior" in cleaned
        assert "Closes #1234." in cleaned
        # Excess blank lines should be collapsed but content preserved.
        assert "feat(scanner): wire up X" in cleaned

    def test_clean_message_passes_through(self):
        msg = "feat: nothing to strip\n\nBody line.\n"
        cleaned, removed = commit_msg_hook.strip_ai_attribution(msg)
        assert removed == 0
        assert "feat: nothing to strip" in cleaned
        assert "Body line." in cleaned

    def test_excessive_blank_lines_collapsed(self):
        msg = "feat: x\n\n\n\n\nBody\n"
        cleaned, _ = commit_msg_hook.strip_ai_attribution(msg)
        assert "\n\n\n" not in cleaned

    def test_trailing_whitespace_stripped(self):
        msg = "feat: x\n\nBody   \n   \n"
        cleaned, _ = commit_msg_hook.strip_ai_attribution(msg)
        assert cleaned.endswith("\n")
        assert not cleaned.endswith("   \n")


