def get_techniques_by_category(category: AttackCategory) -> list[AttackTechnique]:
    """Get all techniques in a specific category."""
    return [t for t in AURA_ATTACK_TAXONOMY if t.category == category]


def get_technique_by_id(technique_id: str) -> Optional[AttackTechnique]:
    """Get a specific technique by ID."""
    for t in AURA_ATTACK_TAXONOMY:
        if t.technique_id == technique_id:
            return t
    return None
