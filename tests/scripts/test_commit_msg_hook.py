"""
Tests for scripts/security_hooks/commit_msg_hook.py.

Closes #163 HIGH item: "No test coverage for commit_msg_hook.py".

Covers:
- Standard Co-Authored-By stripping
- Versioned Claude variants (Opus/Sonnet/Haiku)
- "Generated with Claude Code" markdown + plain forms
- Robot emoji + Claude lines
- Idempotence (running twice is a no-op)
- Multi-paragraph commit bodies preserved
- File-not-found and empty-message handling
"""

from __future__ import annotations

import importlib.util
import sys
from pathlib import Path

import pytest

# Load the hook module by path (it's a script, not a package).
_HOOK_PATH = (
    Path(__file__).parent.parent.parent
    / "scripts"
    / "security_hooks"
    / "commit_msg_hook.py"
)
_spec = importlib.util.spec_from_file_location("commit_msg_hook", _HOOK_PATH)
commit_msg_hook = importlib.util.module_from_spec(_spec)
_spec.loader.exec_module(commit_msg_hook)


# ---------------------------------------------------------------------------
# strip_ai_attribution unit tests
# ---------------------------------------------------------------------------


class TestStripCoAuthoredBy:
    """Co-Authored-By lines for Claude must be stripped."""

    def test_strips_basic_co_authored_by(self):
        msg = (
            "feat: add thing\n"
            "\n"
            "Co-Authored-By: Claude <noreply@anthropic.com>\n"
        )
        cleaned, removed = commit_msg_hook.strip_ai_attribution(msg)
        assert removed == 1
        assert "Claude" not in cleaned
        assert "Co-Authored-By" not in cleaned
        assert "feat: add thing" in cleaned

    def test_strips_versioned_claude_variants(self):
        for variant in (
            "Claude Opus 4.5",
            "Claude Opus 4.7",
            "Claude Sonnet 4.6",
            "Claude Haiku 4.5",
            "Claude 3.5",
        ):
            msg = f"fix: bug\n\nCo-Authored-By: {variant} <noreply@anthropic.com>\n"
            cleaned, removed = commit_msg_hook.strip_ai_attribution(msg)
            assert removed == 1, f"failed to strip variant: {variant}"
            assert variant not in cleaned

    def test_strips_case_insensitive(self):
        msg = (
            "feat: thing\n"
            "\n"
            "co-authored-by: claude <user@anthropic.com>\n"
        )
        cleaned, removed = commit_msg_hook.strip_ai_attribution(msg)
        assert removed == 1
        assert "claude" not in cleaned

    def test_keeps_other_co_authors(self):
        msg = (
            "feat: pair-programmed\n"
            "\n"
            "Co-Authored-By: Alice <alice@example.com>\n"
            "Co-Authored-By: Claude <noreply@anthropic.com>\n"
        )
        cleaned, removed = commit_msg_hook.strip_ai_attribution(msg)
        assert removed == 1
        assert "Alice <alice@example.com>" in cleaned
        assert "Claude" not in cleaned


