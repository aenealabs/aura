class TestMain:
    """End-to-end behavior of the hook entry point."""

    def test_main_strips_in_place(self, tmp_path, monkeypatch):
        commit_msg = tmp_path / "COMMIT_EDITMSG"
        commit_msg.write_text(
            "feat: a thing\n\nCo-Authored-By: Claude <noreply@anthropic.com>\n",
            encoding="utf-8",
        )
        monkeypatch.setattr(sys, "argv", ["commit_msg_hook.py", str(commit_msg)])
        rc = commit_msg_hook.main()
        assert rc == 0
        cleaned = commit_msg.read_text(encoding="utf-8")
        assert "Claude" not in cleaned
        assert "feat: a thing" in cleaned

    def test_main_no_attribution_passes_unchanged(self, tmp_path, monkeypatch):
        commit_msg = tmp_path / "COMMIT_EDITMSG"
        original = "fix: clean message\n\nNothing to strip.\n"
        commit_msg.write_text(original, encoding="utf-8")
        monkeypatch.setattr(sys, "argv", ["commit_msg_hook.py", str(commit_msg)])
        rc = commit_msg_hook.main()
        assert rc == 0
        assert commit_msg.read_text(encoding="utf-8") == original

    def test_main_missing_file_returns_zero(self, tmp_path, monkeypatch):
        """A missing commit-msg file must not block the commit."""
        missing = tmp_path / "does_not_exist"
        monkeypatch.setattr(sys, "argv", ["commit_msg_hook.py", str(missing)])
        rc = commit_msg_hook.main()
        assert rc == 0

    def test_main_no_argv_returns_zero(self, monkeypatch):
        """Hook must not raise when invoked without args."""
        monkeypatch.setattr(sys, "argv", ["commit_msg_hook.py"])
        rc = commit_msg_hook.main()
        assert rc == 0

    def test_main_empty_file_returns_zero(self, tmp_path, monkeypatch):
        commit_msg = tmp_path / "COMMIT_EDITMSG"
        commit_msg.write_text("", encoding="utf-8")
        monkeypatch.setattr(sys, "argv", ["commit_msg_hook.py", str(commit_msg)])
        rc = commit_msg_hook.main()
        assert rc == 0


